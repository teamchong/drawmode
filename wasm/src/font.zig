test "drawChar renders A" {
    var buf: [8 * 16 * 4]u8 = [_]u8{0} ** (8 * 16 * 4);
    drawChar(&buf, 8, 16, 0, 0, 'A', .{ 255, 0, 0, 255 });
    // 'A' glyph row 2 (0x10 = 00010000) should have pixel at col 4
    // Row 3 (0x38 = 00111000) should have pixels at cols 2,3,4
    var has_pixel = false;
    for (buf, 0..) |b, i| {
        if (i % 4 == 0 and b == 255) {
            has_pixel = true;
            break;
        }
    }
    try std.testing.expect(has_pixel);
}

test "drawString renders text" {
    var buf: [800 * 20 * 4]u8 = [_]u8{0} ** (800 * 20 * 4);
    const w = drawString(&buf, 800, 20, 0, 0, "Hello", 16, .{ 0, 0, 0, 255 });
    try std.testing.expectEqual(@as(u32, 40), w); // 5 chars × 8px
}

test "measureString" {
    try std.testing.expectEqual(@as(u32, 40), measureString("Hello", 16));
    try std.testing.expectEqual(@as(u32, 80), measureString("Hello", 32)); // scale 2
}
