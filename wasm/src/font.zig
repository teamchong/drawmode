const std = @import("std");

/// 8×16 bitmap font for printable ASCII (codepoints 32–126).
/// Each glyph is 16 rows of 8 pixels, stored as 16 bytes (MSB = leftmost pixel).
/// Total data: 95 glyphs × 16 bytes = 1520 bytes.

pub const GLYPH_W: u32 = 8;
pub const GLYPH_H: u32 = 16;
const FIRST_CHAR: u8 = 32;
const LAST_CHAR: u8 = 126;

/// Draw a single character glyph into an RGBA pixel buffer.
/// x, y = top-left corner of the glyph.
/// stride = number of pixels per row in the buffer.
pub fn drawChar(
    buf: []u8,
    buf_w: u32,
    buf_h: u32,
    x: i32,
    y: i32,
    char: u8,
    color: [4]u8,
) void {
    if (char < FIRST_CHAR or char > LAST_CHAR) return;

    const glyph_idx: usize = char - FIRST_CHAR;
    const glyph = &glyphs[glyph_idx];

    var row: u32 = 0;
    while (row < GLYPH_H) : (row += 1) {
        const py = y + @as(i32, @intCast(row));
        if (py < 0 or py >= @as(i32, @intCast(buf_h))) continue;

        const bits = glyph[row];
        var col: u32 = 0;
        while (col < GLYPH_W) : (col += 1) {
            const px = x + @as(i32, @intCast(col));
            if (px < 0 or px >= @as(i32, @intCast(buf_w))) continue;

            if ((bits >> @intCast(7 - col)) & 1 == 1) {
                const offset: usize = (@as(usize, @intCast(py)) * buf_w + @as(usize, @intCast(px))) * 4;
                if (offset + 3 < buf.len) {
                    buf[offset] = color[0];
                    buf[offset + 1] = color[1];
                    buf[offset + 2] = color[2];
                    buf[offset + 3] = color[3];
                }
            }
        }
    }
}

/// Draw a string of text into an RGBA pixel buffer.
/// Supports scaling via nearest-neighbor for fontSize > 16.
/// Returns the width in pixels of the rendered text.
pub fn drawString(
    buf: []u8,
    buf_w: u32,
    buf_h: u32,
    x: i32,
    y: i32,
    text: []const u8,
    font_size: u32,
    color: [4]u8,
) u32 {
    if (font_size == 0) return 0;

    // Scale factor: 1 at fontSize <= 16, otherwise fontSize / 16
    const scale = if (font_size <= GLYPH_H) 1 else font_size / GLYPH_H;
    const char_w: u32 = GLYPH_W * scale;
    const char_h: u32 = GLYPH_H * scale;

    var cx: i32 = x;
    for (text) |ch| {
        if (ch < FIRST_CHAR or ch > LAST_CHAR) {
            cx += @intCast(char_w);
            continue;
        }

        if (scale == 1) {
            drawChar(buf, buf_w, buf_h, cx, y, ch, color);
        } else {
            drawCharScaled(buf, buf_w, buf_h, cx, y, ch, color, scale, char_w, char_h);
        }
        cx += @intCast(char_w);
    }

    return @intCast(@as(i32, @intCast(text.len)) * @as(i32, @intCast(char_w)));
}

/// Draw a character with nearest-neighbor scaling.
fn drawCharScaled(
    buf: []u8,
    buf_w: u32,
    buf_h: u32,
    x: i32,
    y: i32,
    char: u8,
    color: [4]u8,
    scale: u32,
    char_w: u32,
    char_h: u32,
) void {
    if (char < FIRST_CHAR or char > LAST_CHAR) return;
    const glyph_idx: usize = char - FIRST_CHAR;
    const glyph = &glyphs[glyph_idx];

    var row: u32 = 0;
    while (row < char_h) : (row += 1) {
        const py = y + @as(i32, @intCast(row));
        if (py < 0 or py >= @as(i32, @intCast(buf_h))) continue;

        // Map scaled row back to glyph row
        const src_row = row / scale;
        if (src_row >= GLYPH_H) continue;
        const bits = glyph[src_row];

        var col: u32 = 0;
        while (col < char_w) : (col += 1) {
            const px = x + @as(i32, @intCast(col));
            if (px < 0 or px >= @as(i32, @intCast(buf_w))) continue;

            const src_col = col / scale;
            if (src_col >= GLYPH_W) continue;

            if ((bits >> @intCast(7 - src_col)) & 1 == 1) {
                const offset: usize = (@as(usize, @intCast(py)) * buf_w + @as(usize, @intCast(px))) * 4;
                if (offset + 3 < buf.len) {
                    buf[offset] = color[0];
                    buf[offset + 1] = color[1];
                    buf[offset + 2] = color[2];
                    buf[offset + 3] = color[3];
                }
            }
        }
    }
}

/// Measure the pixel width of a string at a given font size.
pub fn measureString(text: []const u8, font_size: u32) u32 {
    if (font_size == 0) return 0;
    const scale = if (font_size <= GLYPH_H) 1 else font_size / GLYPH_H;
    return @intCast(text.len * GLYPH_W * scale);
}

